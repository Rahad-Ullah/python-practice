#pair numbers among 1-100
for x in range(2, 100+1, 2):
    print(x)

#non-pair numbers among 1-100
for x in range(1, 100+1, 2):
    print(x)

#pair numbers among 1-100_using while loop
i = 2
while i<=100:
    print(i)
    i = i + 2

